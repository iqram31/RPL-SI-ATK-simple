    private void btnPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihActionPerformed
        int baris = tabelPilihPegawai.getSelectedRow();
        if (baris >= 0) {
            pegawai = arrayPegawai.get(baris);
            dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Silakan pilih Pegawai");
        }
    }//GEN-LAST:event_btnPilihActionPerformed

