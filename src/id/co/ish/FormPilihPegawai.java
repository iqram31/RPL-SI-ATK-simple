    private void getData() {
        PegawaiDAO dao = new PegawaiDAOImpl();
        arrayPegawai = dao.getPegawai();

        PilihPegawaiTM tm = new PilihPegawaiTM();
        tm.setArrayPegawai(arrayPegawai);
        tabelPilihPegawai.setModel(tm);
    }

    private void getData(String cari) {
        PegawaiDAO dao = new PegawaiDAOImpl();
        arrayPegawai = dao.getPegawai(cari);

        PilihPegawaiTM tm = new PilihPegawaiTM();
        tm.setArrayPegawai(arrayPegawai);
        tabelPilihPegawai.setModel(tm);
    }

