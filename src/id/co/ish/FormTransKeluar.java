    //Prosedur memanggil data di tabel barang
    private void getData() {
        TransaksiKeluarDAO dao = new TransaksiKeluarDAOImpl();
        arrayTransaksiKeluar = dao.getTransKeluar();

        TransaksiKeluarTM tk = new TransaksiKeluarTM();
        tk.setArrayTransKeluar(arrayTransaksiKeluar);
        tabelTransKeluar.setModel(tk);

    }

    //Prosedur memanggail data yang sudah dicari
    private void getData(String cari) {
        TransaksiKeluarDAO dao = new TransaksiKeluarDAOImpl();
        arrayTransaksiKeluar = dao.getTransKeluar(cari);

        TransaksiKeluarTM tk = new TransaksiKeluarTM();
        tk.setArrayTransKeluar(arrayTransaksiKeluar);
        tabelTransKeluar.setModel(tk);
    }

    //Prosedur untuk me-merefresh data
    private void refreshData() {
        getData();
    }

    //Prosedur untuk menampilkan report
    private void tampilReport() {
        try {
            //File report = new File("src\\id\\co\\ish\\report\\ReportTransKeluar.jrxml");
            InputStream file = getClass().getResourceAsStream("/id/co/ish/report/ReportTransKeluar.jrxml");
            JasperDesign jasperDesign = JRXmlLoader.load(file);
            JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);
            //jasDes = JRXmlLoader.load(file);
            //jasRep = JasperCompileManager.compileReport(jasDes);
            jasPri = JasperFillManager.fillReport(jasperReport, param, koneksi.getConnection());
            JasperViewer.viewReport(jasPri, false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

