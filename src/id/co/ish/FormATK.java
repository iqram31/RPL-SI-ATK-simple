    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int baris = tabelBarang.getSelectedRow();
        //Validasi barang yang terpilih
        if (baris >= 0) {
            Barang barangTerpilih = arrayBarang.get(baris);

            int aksiUser = JOptionPane.showConfirmDialog(this, "Apakah Anda "
                    + "yakin akan menghapus data "
                    + barangTerpilih.getNama(), "Hapus Barang ?",
                    JOptionPane.YES_NO_OPTION);
            if (aksiUser == JOptionPane.YES_NO_OPTION) {
                String kode = barangTerpilih.getKodeBrg();
                BarangDAO dao = new BarangDAOImpl();
                boolean sukses = dao.hapusBarang(kode);

                if (sukses) {
                    JOptionPane.showMessageDialog(this, "Barang berhasil dihapus");
                } else {
                    JOptionPane.showMessageDialog(this, "Barang gagal dihapus");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih barang yang akan dihapus");
        }
        getData(null);
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        int baris = tabelBarang.getSelectedRow();
        //Validasi barang terpilih
        if (baris >= 0) {
            //mengambil data pakaian dari baris tabel
            Barang barangTerpilih = arrayBarang.get(baris);
            FormUbahATK fub = new FormUbahATK(null, true, barangTerpilih);
            fub.setLocationRelativeTo(null);
            fub.setResizable(false);
            fub.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Pilih barang yang akan diubah");
        }
        getData();
    }//GEN-LAST:event_btnUbahActionPerformed

